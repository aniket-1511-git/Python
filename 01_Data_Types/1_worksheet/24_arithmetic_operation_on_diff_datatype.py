'''
Perform arithmetic between integer (10) and float (3.5) variables, then print 
the result and its type.
'''
print("Enter int data")
i=int(input())
print("Enter float data")
f=float(input())
print("Sum = ",f+i)
print("Diff = ",f"{f-i:.2f}")
print("Mul = ",f*i)
print("Divi = ",f/i)
print("Mod = ",f"{f%i:.2f}")