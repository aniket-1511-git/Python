'''Compare two integers and print boolean results for each comparison (==, !=, >, <, >=, <=).
'''
print("Enter num1 ")
x=int(input())
print("Enter num2 ")
y=int(input())
print(x," == ",y,"=",x==y)
print(x," != ",y,"=",x!=y)
print(x," < ",y," =",x<y)
print(x," > ",y," =",x>y)
print(x," <= ",y,"=",x<=y)
print(x," >= ",y,"=",x>=y)