'''
Explain the difference between mutable and immutable data types in Python, with practical examples.
'''
'''
Mutable Data Types
Mutable data types are those that can be changed or modified after their 
creation.

Lists
Dictionaries
Sets
'''
lst = [1, 2, 3]
lst[0] = 10
print(lst)
'''
Immutable Data Types
Immutable data types are those that can not be changed or modified after their 
creation.

Tuples
Strings
'''
st = "Hello"
st[0] = 'h'
print(st)
